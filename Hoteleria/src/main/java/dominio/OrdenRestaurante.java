public class OrdenRestaurante {

    String idEncabezado, idHabitacion, mesaOrden, fechaOrden, horaOrden;

    public String getIdEncabezado() {
        return idEncabezado;
    }

    public void setIdEncabezado(String idEncabezado) {
        this.idEncabezado = idEncabezado;
    }

    public String getIdHabitacion() {
        return idHabitacion;
    }

    public void setIdHabitacion(String idHabitacion) {
        this.idHabitacion = idHabitacion;
    }

    public String getMesaOrden() {
        return mesaOrden;
    }

    public void setMesaOrden(String mesaOrden) {
        this.mesaOrden = mesaOrden;
    }

    public String getFechaOrden() {
        return fechaOrden;
    }

    public void setFechaOrden(String fechaOrden) {
        this.fechaOrden = fechaOrden;
    }

    public String getHoraOrden() {
        return horaOrden;
    }

    public void setHoraOrden(String horaOrden) {
        this.horaOrden = horaOrden;
    }
    
    
    
}
