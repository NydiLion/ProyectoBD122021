public class AsignacionIngresienteMenu {
    String idCorrelativo, idPlato, idProducto, cantidadIngrediente, nombrePlato, unidadPlato;

    public String getIdCorrelativo() {
        return idCorrelativo;
    }

    public void setIdCorrelativo(String idCorrelativo) {
        this.idCorrelativo = idCorrelativo;
    }

    public String getIdPlato() {
        return idPlato;
    }

    public void setIdPlato(String idPlato) {
        this.idPlato = idPlato;
    }

    public String getIdProducto() {
        return idProducto;
    }

    public void setIdProducto(String idProducto) {
        this.idProducto = idProducto;
    }

    public String getCantidadIngrediente() {
        return cantidadIngrediente;
    }

    public void setCantidadIngrediente(String cantidadIngrediente) {
        this.cantidadIngrediente = cantidadIngrediente;
    }

    public String getNombrePlato() {
        return nombrePlato;
    }

    public void setNombrePlato(String nombrePlato) {
        this.nombrePlato = nombrePlato;
    }

    public String getUnidadPlato() {
        return unidadPlato;
    }

    public void setUnidadPlato(String unidadPlato) {
        this.unidadPlato = unidadPlato;
    }
}
