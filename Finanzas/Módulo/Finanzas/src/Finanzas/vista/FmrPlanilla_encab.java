    public void llenadoDeTablas() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID");
        modelo.addColumn("Percepciones");
        modelo.addColumn("Deducciones");
        modelo.addColumn("Total Liquido");
        Planilla_encDAO encDAO = new Planilla_encDAO();
        List<Planilla_enc> encabs = encDAO.select();
        Tabla.setModel(modelo);
        Object[] objeto = new Object[4];
        for (int i = 0; i < encabs.size(); i++) {
            objeto[0] = encabs.get(i).getId_planen();
            objeto[1] = encabs.get(i).getTotal_percep();
            objeto[2] = encabs.get(i).getTotal_deduc();
            objeto[3] = encabs.get(i).getTotal_liq();


            modelo.addRow(objeto);
        }
    }
