    public void llenadoDeTablas() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID");
        modelo.addColumn("Nombre");
        modelo.addColumn("Apellido");
        modelo.addColumn("DPI");
        modelo.addColumn("Correo");
        modelo.addColumn("Puesto");
        modelo.addColumn("Estado");
        modelo.addColumn("Contratado");
        ConceptopDAO conceptospDAO = new ConceptopDAO();
        List<Conceptop> conceptosp = conceptospDAO.select();
        Tabla.setModel(modelo);
        Object[] objeto = new Object[5];
        for (int i = 0; i < conceptosp.size(); i++) {
            objeto[0] = conceptosp.get(i).getId_conceptop();
            objeto[1] = conceptosp.get(i).getNombre_conceptop();
            objeto[2] = conceptosp.get(i).getTipo_conceptop();
            objeto[3] = conceptosp.get(i).getClase_conceptop();
            objeto[4] = conceptosp.get(i).getValor_conceptop();

            modelo.addRow(objeto);
        }
    }
